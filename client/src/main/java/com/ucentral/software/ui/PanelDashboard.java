  private void transactionsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transactionsButtonActionPerformed
    try {

      String endDateText = transactionsEndDateField.getText();
      LocalDate endDate = LocalDate.parse(endDateText, DATE_FORMAT);

      String startDateText = transactionsStartDateField.getText();
      LocalDate startDate = LocalDate.parse(startDateText, DATE_FORMAT);

      if (startDate.isAfter(endDate)) {
        JOptionPane.showMessageDialog(this, "La fecha de inicio no puede ser mayor que la fecha de fin.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
      }

      List<Transaction> transactions = service.getTransactionByDate(startDate, endDate);

      if (transactions.isEmpty()) {
        transactionsTextArea.setText("No hay movimientos en este período.");
      }
      StringBuilder sb = new StringBuilder();
      for (Transaction t : transactions) {
        sb.append("Fecha: ").append(t.getDate()).append(" | ")
                .append("Descripción: ").append(t.getDescription()).append(" | ")
                .append("Monto: ").append(t.getAmount()).append("\n");
      }
      transactionsTextArea.setText(sb.toString());

    } catch (DateTimeParseException e) {
      JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use yyyy-MM-dd.", "Error", JOptionPane.ERROR_MESSAGE);
    }
  }//GEN-LAST:event_transactionsButtonActionPerformed

  public void init() {
    clearFileds();
    refreshData();
    fillComboBox();
    enableApplyButton();
  }

  private void clearFileds() {
    debtField.setText("0");
    quotaField.setText("0");
    applyButton.setEnabled(Boolean.TRUE);
    selectedCreditCard.setEnabled(Boolean.TRUE);
  }

  private void refreshData() {
    service.account();
  }

  private void fillComboBox() {

    selectedCreditCard.removeAllItems();
    var cards = service.getCreditCards();

    if (cards.isEmpty()) {
      selectedCreditCard.setEnabled(Boolean.FALSE);
      return;
    }

    cards.forEach(card -> {
      selectedCreditCard.addItem(card.getNumber());
    });

    selectedCreditCard.setEnabled(Boolean.TRUE);

  }

  private void enableApplyButton() {

    var cards = service.getCreditCards();
    Boolean isEnable = cards.isEmpty() || cards.size() < 3;
    applyButton.setEnabled(isEnable);

  }

  private void onComboBoxChange() {

    var numberCard = selectedCreditCard.getSelectedItem();

    if (numberCard == null) {
      return;
    }

    service.setSelectedCreditCard(numberCard.toString());

    var card = service.getCreditCard();
    quotaField.setText(card.getBalance().toString());
    debtField.setText(card.getDebt().toString());

  }


