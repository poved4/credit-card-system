    private void savePurchaseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savePurchaseButtonActionPerformed
      try {
        LocalDate fecha = LocalDate.parse(purchaseDateField.getText(), DATE_FORMAT);
        Double precio = Double.valueOf(purchasePriceField.getText());
        String descripcion = purchaseTextArea.getText();

        if (descripcion.isEmpty()) {
          JOptionPane.showMessageDialog(this, "La descripción no puede estar vacía.", "Error", JOptionPane.ERROR_MESSAGE);
          return;
        }

        String response = service.registerPurchase(fecha, descripcion, precio);
        JOptionPane.showMessageDialog(this, response, "Información", JOptionPane.INFORMATION_MESSAGE);

      } catch (DateTimeParseException e) {
        JOptionPane.showMessageDialog(this, "Formato de fecha inválido. Use yyyy-MM-dd.", "Error", JOptionPane.ERROR_MESSAGE);
      } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese un precio válido.", "Error", JOptionPane.ERROR_MESSAGE);
      }
    }//GEN-LAST:event_savePurchaseButtonActionPerformed

